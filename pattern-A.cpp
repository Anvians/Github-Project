#include <iostream>
using namespace std;
int main()
{
    int i, j, n;
    int mid = n / 2;
    cout << "Enter the value of n";
    cin >> n;
    for (i = 0; i < j; i++)
    {
        for (j = 0; j < n; j++)
        {
            if ((i + j >= mid ))
            {
                cout << "*";
            }
           
        }
        cout << endl;
    }
    return 0;
}